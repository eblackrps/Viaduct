import http from "node:http";
import os from "node:os";
import path from "node:path";
import { spawn } from "node:child_process";
import { fileURLToPath } from "node:url";
import { mkdirSync, mkdtempSync, rmSync, writeFileSync } from "node:fs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const webDir = path.resolve(__dirname, "..");
const repoDir = path.resolve(webDir, "..");
const runtimeDir = mkdtempSync(path.join(os.tmpdir(), "viaduct-runtime-e2e-"));
const configPath = path.join(runtimeDir, "config.yaml");
const policiesDir = path.join(runtimeDir, "policies");
const runtimeBaseURL = "http://127.0.0.1:4174";
const readinessPort = 4175;
const startTime = Date.now();

let shuttingDown = false;
let ready = false;
let readinessError = "";

mkdirSync(policiesDir, { recursive: true });
writeFileSync(
	configPath,
	[
		"# Runtime smoke config generated by Playwright.",
		"insecure: false",
		"sources:",
		`  kvm:`,
		`    address: ${JSON.stringify(path.join(repoDir, "examples", "lab", "kvm").replaceAll("\\", "/"))}`,
		"",
	].join("\n"),
	"utf8",
);
writeFileSync(
	path.join(policiesDir, "README.md"),
	"# Runtime smoke policy placeholder\n",
	"utf8",
);

const readinessServer = http.createServer((_request, response) => {
	const payload = ready
		? { status: "ready" }
		: { status: "starting", error: readinessError || undefined };
	response.writeHead(ready ? 200 : 503, { "Content-Type": "application/json" });
	response.end(JSON.stringify(payload));
});
readinessServer.listen(readinessPort, "127.0.0.1");

const child = spawn(
	"go",
	[
		"run",
		"./cmd/viaduct",
		"start",
		"--config",
		configPath,
		"--port",
		"4174",
		"--host",
		"127.0.0.1",
		"--open-browser=false",
	],
	{
		cwd: repoDir,
		stdio: "inherit",
		env: process.env,
	},
);

console.log(`Viaduct runtime smoke config: ${configPath}`);

void waitForRuntimeReady()
	.then(() => {
		ready = true;
		readinessError = "";
	})
	.catch(async (error) => {
		readinessError = error instanceof Error ? error.message : String(error);
		console.error(readinessError);
		process.exitCode = 1;
		await shutdown();
	});

child.on("error", (error) => {
	readinessError = `failed to start the Viaduct runtime smoke server: ${error.message}`;
	console.error(readinessError, error);
	process.exitCode = 1;
	void shutdown();
});

child.on("exit", (code, signal) => {
	if (!shuttingDown) {
		const earlyExit = Date.now() - startTime < 2_000;
		readinessError = earlyExit
			? `Viaduct runtime smoke server exited before 2s (code=${code ?? "null"}, signal=${signal ?? "none"}). Check ${configPath} and the runtime output above.`
			: `Viaduct runtime smoke server exited unexpectedly (code=${code ?? "null"}, signal=${signal ?? "none"}).`;
		console.error(readinessError);
		process.exitCode = code ?? 1;
	}
	void shutdown();
});

for (const signal of ["SIGINT", "SIGTERM"]) {
	process.on(signal, () => {
		void shutdown(signal);
	});
}

async function waitForRuntimeReady() {
	await waitForEndpoint(`${runtimeBaseURL}/readyz`);
	await waitForEndpoint(`${runtimeBaseURL}/api/v1/ping`);

	const sessionCookie = await createLocalRuntimeSession();
	await waitForEndpoint(`${runtimeBaseURL}/api/v2/inventory?per_page=1`, {
		headers: {
			Cookie: sessionCookie,
		},
	});
}

async function createLocalRuntimeSession() {
	const response = await fetch(`${runtimeBaseURL}/api/v1/auth/session`, {
		method: "POST",
		headers: {
			"Content-Type": "application/json",
			Origin: runtimeBaseURL,
		},
		body: JSON.stringify({ mode: "local" }),
	});
	if (!response.ok) {
		throw new Error(
			`runtime auth session bootstrap failed with status ${response.status}`,
		);
	}

	const cookieHeader = response.headers.get("set-cookie");
	if (!cookieHeader) {
		throw new Error("runtime auth session bootstrap did not return a session cookie");
	}

	return cookieHeader.split(";")[0];
}

async function waitForEndpoint(url, options = {}) {
	const deadline = Date.now() + 55_000;
	let lastError = new Error(`timed out waiting for ${url}`);

	while (Date.now() < deadline) {
		try {
			const response = await fetch(url, {
				redirect: "manual",
				...options,
			});
			if (response.ok) {
				return;
			}
			lastError = new Error(`${url} returned ${response.status}`);
		} catch (error) {
			lastError =
				error instanceof Error ? error : new Error(String(error));
		}
		await new Promise((resolve) => setTimeout(resolve, 250));
	}

	throw lastError;
}

async function shutdown(signal) {
	if (shuttingDown) {
		return;
	}
	shuttingDown = true;
	ready = false;

	readinessServer.close();
	await stopProcessTree(child.pid);
	cleanupRuntimeDir();
	if (signal) {
		process.exit(0);
	}
}

function cleanupRuntimeDir() {
	try {
		rmSync(runtimeDir, { recursive: true, force: true });
	} catch {}
}

async function stopProcessTree(pid) {
	if (!pid) {
		return;
	}

	if (process.platform === "win32") {
		await new Promise((resolve) => {
			const killer = spawn("taskkill", ["/PID", String(pid), "/T", "/F"], {
				stdio: "ignore",
			});
			killer.on("exit", resolve);
			killer.on("error", resolve);
		});
		return;
	}

	try {
		process.kill(pid, "SIGTERM");
	} catch {
		return;
	}
}
